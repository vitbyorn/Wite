/**
 * КЛАСС ЗВУКОВОГО ДВИЖКА
 * 
 * Использование:
 *   const sound = new SoundEngine();
 *   sound.init();                    // Активировать звук (при клике/таче)
 *   sound.hitPlayer();               // "Бац!" — удар об платформу игрока
 *   sound.hitAI();                   // "Боц!" — удар об платформу AI
 *   sound.wall();                    // "Тык!" — удар об стену
 *   sound.goalPlayer();              // "Дзынь!" — гол противнику
 *   sound.goalAI();                  // "Пыщ!" — гол себе
 *   sound.boost();                   // "Вау!" — ускорение
 *   sound.slow();                    // "Вух!" — замедление
 *   sound.spin();                    // "Вжжик" — закрутка мяча
 *   sound.spark();                   // "Трень, динь, пим..." — искры
 */
class SoundEngine {
  /**
   * Конструктор — создаёт экземпляр звукового движка
   * audioCtx — будет создан при первом вызове init()
   * ready — флаг готовности (true = звук доступен)
   */
  constructor() {
    // Хранилище для аудио-контекста (Web Audio API)
    this.audioCtx = null;
    // Флаг: инициализирован ли звук
    this.ready = false;
    // Флаг: был ли вызван init() хотя бы раз
    this.initialized = false;
  }

  // ================================================================
  // 1. ИНИЦИАЛИЗАЦИЯ (вызывать при клике/таче пользователя)
  // ================================================================

  /**
   * Инициализация звукового движка
   * Должна вызываться ПОСЛЕ взаимодействия пользователя с экраном
   * (клик, тач, движение мыши)
   * 
   * @returns {boolean} true — звук готов, false — ошибка
   */
  init() {
    try {
      // Если аудио-контекст ещё не создан — создаём
      if (!this.audioCtx) {
        // Используем window.AudioContext или webkitAudioContext (для Safari)
        const AudioCtx = window.AudioContext || window.webkitAudioContext;
        this.audioCtx = new AudioCtx();
      }

      // Если контекст в состоянии "suspended" (приостановлен) — возобновляем
      if (this.audioCtx.state === 'suspended') {
        this.audioCtx.resume();
      }

      // Проверяем, что контекст работает
      this.ready = (this.audioCtx.state === 'running');
      this.initialized = true;

      return this.ready;
    } catch (error) {
      // Если ошибка — звук не доступен
      console.warn('SoundEngine: Ошибка инициализации звука', error);
      this.ready = false;
      this.initialized = false;
      return false;
    }
  }

  /**
   * Принудительная проверка и восстановление звука
   * Полезно если браузер "засыпает" звук
   * 
   * @returns {boolean} true — звук работает
   */
  ensureRunning() {
    // Если не инициализирован — пробуем инициализировать
    if (!this.initialized) {
      return this.init();
    }

    // Если контекст существует, но приостановлен — возобновляем
    if (this.audioCtx && this.audioCtx.state === 'suspended') {
      try {
        this.audioCtx.resume();
        this.ready = (this.audioCtx.state === 'running');
      } catch (e) {
        this.ready = false;
      }
    }

    return this.ready;
  }

  // ================================================================
  // 2. БАЗОВАЯ ФУНКЦИЯ ВОСПРОИЗВЕДЕНИЯ (низкоуровневая)
  // ================================================================

  /**
   * Воспроизвести звук
   * 
   * @param {number} freq - Частота звука в Гц (высота тона)
   * @param {number} duration - Длительность в секундах
   * @param {string} type - Тип волны: 'square', 'sawtooth', 'sine', 'triangle'
   * @param {number} volume - Громкость (0-1)
   */
  play(freq, duration, type = 'square', volume = 0.25) {
    try {
      // Проверяем, что звук инициализирован и работает
      if (!this.ensureRunning()) {
        // Если звук не готов — тихо выходим
        return;
      }

      // Дополнительная проверка контекста
      if (!this.audioCtx || this.audioCtx.state !== 'running') {
        return;
      }

      // === СОЗДАЁМ ОСЦИЛЛЯТОР (генератор звука) ===
      const oscillator = this.audioCtx.createOscillator();
      
      // === СОЗДАЁМ УСИЛИТЕЛЬ (регулировка громкости) ===
      const gain = this.audioCtx.createGain();

      // --- Настройка осциллятора ---
      oscillator.type = type;          // Тип волны: square (квадратная) — для 8-битного звука
      oscillator.frequency.value = freq; // Частота (высота тона)

      // --- Настройка громкости (БЕЗ ЗАТУХАНИЯ!) ---
      const now = this.audioCtx.currentTime;
      
      // Устанавливаем громкость мгновенно (без затухания)
      gain.gain.setValueAtTime(volume, now);
      
      // !!! ВАЖНО: НЕТ exponentialRampToValueAtTime !!!
      // Звук НЕ затухает, а резко обрывается через duration
      // Это даёт эффект "щелчка" — классический 8-битный звук

      // --- Соединяем цепочку: осциллятор → усилитель → выход (колонки) ---
      oscillator.connect(gain);
      gain.connect(this.audioCtx.destination);

      // --- Запускаем и останавливаем ---
      oscillator.start(now);                          // Начать сейчас
      oscillator.stop(now + duration);               // Остановить через duration секунд

    } catch (error) {
      // Если ошибка — просто игнорируем (тихий режим)
      // Это предотвращает падение игры из-за звука
    }
  }

  // ================================================================
  // 3. ВСЕ ЗВУКИ ИГРЫ (СОЧНЫЕ 8-БИТНЫЕ)
  // ================================================================
  // Каждый звук — это вызов play() с определёнными параметрами
  // Названия соответствуют описанию в задании
  // ================================================================

  /**
   * "Бац!" — Удар об платформу игрока
   * Высокий, резкий звук
   */
  hitPlayer() {
    this.play(880, 0.12, 'square', 0.35);
  }

  /**
   * "Боц!" — Удар об платформу противника (AI)
   * Чуть ниже, чем удар игрока
   */
  hitAI() {
    this.play(660, 0.12, 'square', 0.35);
  }

  /**
   * "Тык!" — Удар об стену
   * Короткий, низкий звук
   */
  wall() {
    this.play(440, 0.08, 'square', 0.25);
  }

  /**
   * "Дзынь!" — Гол противнику (игрок забил)
   * Высокий, радостный звук, длиннее обычного
   */
  goalPlayer() {
    this.play(1200, 0.25, 'square', 0.4);
  }

  /**
   * "Пыщ!" — Гол себе (AI забил игроку)
   * Очень низкий, печальный звук
   */
  goalAI() {
    this.play(300, 0.25, 'square', 0.4);
  }

  /**
   * "Вау!" — Ускорение (пока без активации в игре)
   * Высокий, энергичный
   */
  boost() {
    this.play(1000, 0.15, 'square', 0.3);
  }

  /**
   * "Вух!" — Замедление (пока без активации в игре)
   * Низкий, протяжный
   */
  slow() {
    this.play(280, 0.2, 'square', 0.25);
  }

  /**
   * "Вжжик" — Закрутка мяча (пока без активации)
   * Пилообразная волна (sawtooth) — даёт "жужжащий" эффект
   */
  spin() {
    this.play(750, 0.18, 'sawtooth', 0.2);
  }

  /**
   * "Трень, динь, пим..." — Лопанье искр
   * Три коротких звука подряд с повышением
   */
  spark() {
    // Первый звук — "Трень"
    this.play(1400, 0.06, 'square', 0.2);
    
    // Второй звук — "Динь" (через 80 мс)
    setTimeout(() => {
      this.play(1100, 0.06, 'square', 0.2);
    }, 80);
    
    // Третий звук — "Пим" (через 160 мс)
    setTimeout(() => {
      this.play(800, 0.08, 'square', 0.2);
    }, 160);
  }

  // ================================================================
  // 4. ДОПОЛНИТЕЛЬНЫЕ ЗВУКИ (расширение)
  // ================================================================

  /**
   * Короткий "пик" — для старта мяча, кликов и т.д.
   */
  click() {
    this.play(600, 0.06, 'square', 0.15);
  }

  /**
   * "Бип!" — для уведомлений
   */
  beep() {
    this.play(800, 0.1, 'square', 0.2);
  }

  /**
   * "Вжух!" — для активации спецспособностей
   */
  whoosh() {
    this.play(500, 0.2, 'sawtooth', 0.15);
  }

  /**
   * Победный звук — восходящая гамма
   */
  victory() {
    this.play(523, 0.1, 'square', 0.3);
    setTimeout(() => this.play(659, 0.1, 'square', 0.3), 100);
    setTimeout(() => this.play(784, 0.15, 'square', 0.35), 200);
  }

  /**
   * Звук поражения — нисходящая гамма
   */
  defeat() {
    this.play(784, 0.1, 'square', 0.3);
    setTimeout(() => this.play(659, 0.1, 'square', 0.3), 100);
    setTimeout(() => this.play(523, 0.15, 'square', 0.35), 200);
  }

  // ================================================================
  // 5. УТИЛИТЫ
  // ================================================================

  /**
   * Получить статус звука
   * @returns {object} { ready, state, initialized }
   */
  getStatus() {
    return {
      ready: this.ready,
      state: this.audioCtx ? this.audioCtx.state : 'none',
      initialized: this.initialized
    };
  }

  /**
   * Полная переинициализация (сброс)
   * Закрывает старый контекст и создаёт новый
   */
  reset() {
    try {
      if (this.audioCtx) {
        this.audioCtx.close();
      }
    } catch (e) {
      // Игнорируем
    }
    this.audioCtx = null;
    this.ready = false;
    this.initialized = false;
    return this.init();
  }
}

